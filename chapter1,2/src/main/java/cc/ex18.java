package cc;

public class ex18 {
	
	public static void main(String[] args) {
		final int LOOP_COUNT = 10;
		
		// 1. 루프 조건 변수
		int i = 0;
		
		// 2. while loop 조건
		while(i < LOOP_COUNT) {
			
			// 3. 반복 코드
			System.out.println(i + ". hello");
			
			// 4. 루프 조건 변수를 변경
			i = i + 1;
		}
		
		
		
		
		
		
	}
}
